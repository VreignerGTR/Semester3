# String
print("Hello World!")
a = "Hello World~"
print(a)
print(type(a))

# Integer
b = 10
print(b)
print(type(b))

# Float
c = 1.5
print(c)
print(type(c))

# Data Kompleks
d = 3j
print(d)
print(type(d))
z = complex(5 - 3j)
print(z)

# Tipe Data Tuple
e = 10, 5
print(e)
print(type(e))
print(e[0])

# Tipe Data List
f = ["a", "b", "c"]
print(f)
print(type(f))
print(f[0])

# Tipe Data Set
g = {10, 20, 30}
print(g)
print(type(g))

# Tipe Data Frozenset
h = frozenset({1, 2, 3})
print(h)
print(type(h))

# Tipe Data Boolean
i = True
j = False
print(i, j)
print(type(i))
print(type(j))